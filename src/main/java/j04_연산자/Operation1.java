package j04_연산자;

public class Operation1 {
    public static void main(String[] args) {
        int num = 10;
        System.out.println(num);

        // num이 11 이 되게 출력
        num = num +1;
        System.out.println(num);

        System.out.println(++num);
        System.out.println(num);
    }
}
